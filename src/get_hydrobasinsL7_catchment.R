################################################################################
# --- 3. FUNCTION DEFINITION ---
################################################################################

get_hydrobasinsL7_catchment <- function(catchment_id,
                                        target_crs = 3035) {

  catchment_id <- as.character(catchment_id)

  message("Querying Ramsar WFS for HydroBASINS level 07 catchment...")
  resp <- httr::GET(
    url = "https://rsis.ramsar.org/geoserver/ows",
    query = list(
      service = "WFS",
      version = "2.0.0",
      request = "GetFeature",
      typeName = "ramsar_sdi:hybas_1c_7",
      outputFormat = "application/json",
      CQL_FILTER = paste0("hybas_id=", catchment_id)
    )
  )

  if (httr::status_code(resp) != 200) {
    stop(httr::content(resp, as = "text", encoding = "UTF-8"),
         call. = FALSE)
  }

  geojson_text <- httr::content(resp, "text", encoding = "UTF-8")

  catchment_sf <- sf::st_read(geojson_text, quiet = TRUE)

  if (nrow(catchment_sf) == 0) {
    stop("No catchment found for catchment_id '", catchment_id, "' in HydroBASINS level 07.", call. = FALSE)
  }

  # Fix invalid geometries (only where needed)
  bad <- !sf::st_is_valid(catchment_sf)
  if (any(bad)) {
    message("Fixing invalid geometries...")
    catchment_sf[bad, ] <- sf::st_make_valid(catchment_sf[bad, ])
  }

  # Enforce multipolygon, reproject to match the rest of the pipeline (EPSG:3035)
  catchment_sf <- sf::st_cast(catchment_sf, "MULTIPOLYGON")
  catchment_sf <- sf::st_transform(catchment_sf, target_crs)

  countries_for_catchment <- catchmentL7_to_countries[[catchment_id]]
  if (is.null(countries_for_catchment)) {
    stop("No country mapping found for catchment_id '", catchment_id, "'.", call. = FALSE)
  }

  message("Catchment retrieval complete.")

  return(list(
    catchment = catchment_sf,
    countries = countries_for_catchment
  ))
}

################################################################################
# --- 4. D2K EXECUTABLE WRAPPER ---
################################################################################

args <- commandArgs(trailingOnly = TRUE)

if (length(args) != 3) {
  stop("Usage: Rscript get_hydrobasinsL7_catchment.R <catchment_id> <output_catchment_gpkg_path> <output_countries_rds_path>",
       call. = FALSE)
}

catchment_id <- args[1]
if (!(catchment_id %in% names(catchmentL7_to_countries))) {
  stop(
    paste0("Invalid catchment_id: ", catchment_id, ". Must be a known HydroBASINS level 07 HYBAS_ID with EU territory."),
    call. = FALSE
  )
}

output_catchment_gpkg_path <- args[2]
output_countries_rds_path <- args[3]

message("D2K Wrapper Started for retrieving HydroBASINS level 07 catchment.")

tryCatch({

  result <- get_hydrobasinsL7_catchment(catchment_id = catchment_id)
  catchment_sf <- result$catchment

  # Save the output
  sf::st_write(catchment_sf, output_catchment_gpkg_path, delete_layer = TRUE, quiet = TRUE)

  countries_for_catchment <- result$countries

  # Save as .rds for machine/subsequent steps
  saveRDS(countries_for_catchment,
          file = output_countries_rds_path)

  message(paste("D2K Wrapper Finished. Catchment saved to",
                output_catchment_gpkg_path,
                " and country list saved to ",
                output_countries_rds_path))

}, error = function(e) {
  stop(paste("Error during script execution:", e$message))
})
